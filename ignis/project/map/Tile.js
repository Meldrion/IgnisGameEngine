"use strict";

class Tile {

    constructor() {
        this.positionX = 0;
        this.positionY = 0;
        this.tilesetX = 0;
        this.tilesetY = 0;
    }

    getPositionX() {
        return this.positionX;
    }

    getPositionY() {
        return this.positionY;
    }

    getTilesetX() {
        return this.tilesetX;
    }

    getTilesetY() {
        return this.tilesetY;
    }

}